public class StockClass {
    private int st_ID;
    private int b_ID;
    private int sup_ID;
    private String st_Date;
    private int st_Qty;
    private int st_Price;

    /**
     * @return the st_ID
     */
    public int getSt_ID() {
        return st_ID;
    }

    /**
     * @param st_ID the st_ID to set
     */
    public void setSt_ID(int st_ID) {
        this.st_ID = st_ID;
    }

    /**
     * @return the b_ID
     */
    public int getB_ID() {
        return b_ID;
    }

    /**
     * @param b_ID the b_ID to set
     */
    public void setB_ID(int b_ID) {
        this.b_ID = b_ID;
    }

    /**
     * @return the sup_ID
     */
    public int getSup_ID() {
        return sup_ID;
    }

    /**
     * @param sup_ID the sup_ID to set
     */
    public void setSup_ID(int sup_ID) {
        this.sup_ID = sup_ID;
    }

    /**
     * @return the st_Date
     */
    public String getSt_Date() {
        return st_Date;
    }

    /**
     * @param st_Date the st_Date to set
     */
    public void setSt_Date(String st_Date) {
        this.st_Date = st_Date;
    }

    /**
     * @return the st_Qty
     */
    public int getSt_Qty() {
        return st_Qty;
    }

    /**
     * @param st_Qty the st_Qty to set
     */
    public void setSt_Qty(int st_Qty) {
        this.st_Qty = st_Qty;
    }

    /**
     * @return the st_Price
     */
    public int getSt_Price() {
        return st_Price;
    }

    /**
     * @param st_Price the st_Price to set
     */
    public void setSt_Price(int st_Price) {
        this.st_Price = st_Price;
    }
    
}
